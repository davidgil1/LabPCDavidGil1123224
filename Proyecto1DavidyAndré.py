# funcion para dibujar un triangulo
def dibujar_triangulo(x, y, tamaño, color):
    turtle.penup()
    turtle.goto(x, y)
    turtle.pendown()
    turtle.color(color)
    turtle.begin_fill()
    for _ in range(3):
        turtle.forward(tamaño)
        turtle.left(120)
    turtle.end_fill()

# Función para dibujar un trapecio con un color específico
def dibujar_trapecio(x, y, base1_length, base2_length, height, color):
    turtle.speed(1)
    turtle.penup()
    turtle.goto(x, y)
    turtle.pendown()
    turtle.color(color)
    turtle.begin_fill()
    turtle.forward(base1_length)
    turtle.right(60)
    turtle.forward(height)
    turtle.right(120)
    turtle.forward(base2_length)
    turtle.right(120)
    turtle.forward(height)
    turtle.end_fill()

