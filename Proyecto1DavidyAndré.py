# Función para dibujar el panel con el título y los espacios
def dibujar_panel(titulo, narrativa, dibujo_panel,color):

    # Configuración inicial
    panel = turtle.Screen()
    panel.setup(width=600, height=400)
    panel.title(titulo)
    panel.bgcolor("lightgray")

    # Dibujar el título
    titulo_turtle = turtle.Turtle()
    titulo_turtle.penup()
    titulo_turtle.goto(0, 275)
    titulo_turtle.write(titulo, align="center", font=("Arial", 20, "bold"))
    titulo_turtle.hideturtle()

    # Dividir la narrativa en varias líneas
    lineas_narrativa = textwrap.wrap(narrativa, width=60)

    # Dibujar la narrativa línea por línea
    narrativa_turtle = turtle.Turtle()
    narrativa_turtle.penup()
    narrativa_turtle.goto(0, 200)
    for linea in lineas_narrativa:
        narrativa_turtle.write(linea, align="center", font=("Arial", 12))
        narrativa_turtle.goto(0, narrativa_turtle.ycor() - 20)  # Moverse a la siguiente línea
    narrativa_turtle.hideturtle()
    # Dibujar los espacios rectangulares
    # Espacio para dibujar
    turtle.speed(1000)
    dibujar_rectangulo(-500, 50, 1000, 350, "white")
    turtle.color("black")
    turtle.color("black")
    turtle.width(1)
    dibujo_panel(color)
    return panel

# Función para dibujar un rectángulo en la posición dada
def dibujar_rectangulo(x, y, ancho, alto, color):
    rectangulo = turtle.Turtle()
    rectangulo.penup()
    rectangulo.color(color)
    rectangulo.goto(x, y)
    rectangulo.pendown()
    rectangulo.begin_fill()
    for _ in range(2):
        rectangulo.forward(ancho)
        rectangulo.right(90)
        rectangulo.forward(alto)
        rectangulo.right(90)
    rectangulo.end_fill()
    rectangulo.hideturtle()

