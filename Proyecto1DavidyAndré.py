# Pedir el nombre del nino

def pedir_nombre():
    while True:
        nombre = input("Por favor, ingresa tu nombre: ")
        if nombre.strip():  # Verifica que el nombre no esté vacío
            return nombre
        else:
            print("El nombre no puede estar vacío. Por favor, inténtalo de nuevo.")

# Pedir la edad del nino
def pedir_edad():
    while True:
        edad = input("Por favor, ingresa tu edad: ")
        if edad.isdigit():  # Verifica que la edad sea un número entero
            return int(edad)
        else:
            print("La edad debe ser un número entero. Por favor, inténtalo de nuevo.")

# Pedir los colores favoritos, en forma de listado 

def pedir_color():
    colores_disponibles = {"rojo": "#FF0000", "azul": "#0000FF", "verde": "#00FF00", "negro": "#000000", "rosado": "#FFC0CB"}
    while True:
        print("Elige tu color favorito:")
        for i, color in enumerate(colores_disponibles.keys(), start=1):
            print(f"{i}. {color}")
        opcion = input("Ingresa el número correspondiente a tu color favorito: ")
        if opcion.isdigit() and 1 <= int(opcion) <= len(colores_disponibles):
            seleccion = list(colores_disponibles.keys())[int(opcion) - 1]
            return colores_disponibles[seleccion]
        else:
            print("Opción inválida. Por favor, elige un número válido.")
